package ex_05_TypeCasting;

public class day_12_task_1 {
    public static void main(String[] args) {
        /*
        Implicit Typecasting (Widening)
        Description: Convert int A =100 to double.
        */
//      Assigned integer A with value 100
        int A = 100;
//      Assigned double variable B with value of A using auto implicit type casting.
        double B = A;
//      The value will be 100.0
        System.out.println(B);



    }
}
