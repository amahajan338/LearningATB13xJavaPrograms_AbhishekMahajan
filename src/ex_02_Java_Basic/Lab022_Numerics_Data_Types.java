package ex_02_Java_Basic;

public class Lab022_Numerics_Data_Types {
    public static void main(String[] args) {
        //Below are the different type of Datatype we can use in java.
        byte b = 100;
        // b = 128;
//        b = -127;
        int i = 10;
        short s = 90;
        char c= 'a';
        //When we are using long datatype we need to add 'l'/'L' in the last of the variable.
        long l = 9876543210l;
        long l1 = 9876543210L;
        System.out.println(b);
        System.out.println(i);
        System.out.println(s);
        System.out.println(c);
        System.out.println(l);
        System.out.println(l1);
    }
}
