package ex_02_Java_Basic;

public class Lab020_Variables {
    public static void main(String[] args){
        //Can variable name be same as keyword or Identifiers or datatype, No:
        //int enum = 23;
        //As java is case-sensitive We can use them
        int Enum = 45;

        //Can we use symbol in the name of variable, Yes:
        int a_b = 34;
        int a$b = 45;
        //Note Math operation symbols (+,&,%,-, etc) cannot be used.

        //Can we have numbers in the name of variable, Yes:
        int abd234 = 41;
    }
}
