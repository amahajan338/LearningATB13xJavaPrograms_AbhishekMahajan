package ex_02_Java_Basic;

public class Lab017_Variables {
    public static void main(String[] args){
        //Declaring the variable with type int and setting the value of variable.
        int age = 76;

        //Setting another value of the variable.
        age = age + 1;

        //Setting the variable value again to initial value.
        age = 76;

        System.out.println(age);
    }
}
